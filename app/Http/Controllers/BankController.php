     */
    public function update(Request $request, $id)
    {
        $this->validate($request, [
            'nama' => 'required'
        ]);

        $bank = Bank::findorfail($id);

        $bank_data = [
            'nama' => $request->nama
        ];

        $bank->update($bank_data);

        return redirect ('/bank');
